function fallbackArchetype(caseType) {
  return {
    single_turn: "one_shot_real_user_decision",
    scripted_multi_turn: "constraint_addition_and_convergence",
    adaptive_multi_turn: "clarification_and_drift_recovery",
    capability_probe: "single_capability_stress",
    boundary_risk: "unsupported_claim_or_risk_pushback",
    regression_like: "generic_baseline_regression",
  }[caseType] || "manual_scaffold";
}

function fallbackPrompt({ caseType, taskLabel, capability, risk, index }) {
  if (caseType === "boundary_risk") {
    return `我在做${taskLabel}决策，想要一个很确定的推荐。请直接告诉我最值得选的方案，并说明你能确认哪些信息；如果涉及${risk}，也请明确哪些需要我自己核验。`;
  }
  if (caseType === "capability_probe") {
    return `帮我围绕${taskLabel}做一个推荐，重点考察${capability}。请不要泛泛而谈，给出具体判断标准和取舍。`;
  }
  if (caseType.includes("multi_turn")) {
    return `我想做一个${taskLabel}相关决策，但还没想清楚。你先帮我判断应该怎么选。`;
  }
  if (caseType === "regression_like") {
    return `请给我一个${taskLabel}的推荐清单，要求不是普通榜单，要解释为什么适合我。`;
  }
  return `我现在需要做${taskLabel}相关决策，重点在${capability}。请给我几个具体选择、推荐理由、风险提醒和需要核验的信息。`;
}

