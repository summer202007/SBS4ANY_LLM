export function buildManualTemplateWorkbook(activeTask) {
  const rows = caseTypes.map((caseType) => ({
    sheetName: caseType,
    rows: [
      columns,
      exampleRow(caseType, activeTask),
      emptyRow(caseType),
      emptyRow(caseType),
      emptyRow(caseType),
    ],
  }));
  rows.unshift({
    sheetName: "README",
    rows: [
      ["SBS Manual Eval Package Template"],
      ["Fill one row per eval case. Keep the file as XML Spreadsheet format when uploading."],
      ["Required columns: caseId, caseType, scenario, userPersona, userGoal, modelFacingPrompt, expectedOutcome, failureModesToProbe."],
      ["List fields can use semicolon-separated values."],
      ["For multi-turn cases, fill turn2UserMessage and turn3UserMessage when you want a fixed guided script."],
      ["Supported caseType values", caseTypes.join("; ")],
    ],
  });
  return xmlWorkbook(rows);
}

export function parseManualTemplateWorkbook(xmlText, activeTask) {
  const sheets = parseWorksheets(xmlText);
  const cases = [];
  for (const caseType of caseTypes) {
    const rows = sheets[caseType] || [];
    if (rows.length < 2) continue;
    const header = rows[0].map((item) => item.trim());
    for (const row of rows.slice(1)) {
      const record = Object.fromEntries(header.map((key, index) => [key, String(row[index] || "").trim()]));
      if (!record.caseId && !record.modelFacingPrompt && !record.scenario) continue;
      cases.push(recordToEvalCase({ ...record, caseType: record.caseType || caseType }, activeTask, cases.length + 1));
    }
  }
  if (!cases.length) throw new Error("No filled eval cases found in the uploaded template.");
  return buildRuntimePackageFromCases(cases, activeTask);
}

export function buildFallbackRuntimePackage(activeTask, caseCountTarget = 15, reason = "") {
  const count = [12, 15, 20].includes(Number(caseCountTarget)) ? Number(caseCountTarget) : 15;
  const plan = fallbackCaseTypePlan(count);
  const task = activeTask?.taskSpace || {};
  const arena = activeTask?.arena || {};
  const challenger = arena.challenger?.name || "Challenger";
  const baseline = arena.baseline?.name || "Doubao";
  const taskLabel = task.label || "目标任务空间";
  const audience = task.targetAudience || "目标用户";
  const capabilities = Array.isArray(task.mustCoverCapabilities) && task.mustCoverCapabilities.length
    ? task.mustCoverCapabilities
    : ["核心任务完成", "约束理解", "证据可靠性", "风险处理"];
  const risks = Array.isArray(task.riskAreas) && task.riskAreas.length ? task.riskAreas : ["来源可信性", "不确定性表达"];
  const records = plan.map((caseType, index) => {
    const capability = capabilities[index % capabilities.length];
    const risk = risks[index % risks.length];
    const n = String(index + 1).padStart(3, "0");
    return {
      caseId: `fallback-${caseType.replaceAll("_", "-")}-${n}`,
      caseType,
      capabilityCluster: capability,
      scenarioArchetype: fallbackArchetype(caseType),
      scenario: `${taskLabel}：面向${audience}，测试「${capability}」场景。`,
      userPersona: audience,
      userGoal: `在${taskLabel}中获得能帮助真实决策的建议。`,
      modelFacingPrompt: fallbackPrompt({ caseType, taskLabel, capability, risk, index }),
      expectedOutcome: `答案应围绕${capability}给出具体、可执行、符合用户背景的建议，并处理${risk}。`,
      mustDo: `理解用户任务; 覆盖${capability}; 给出可执行建议; 标注不确定性`,
      mustNotDo: `忽略核心约束; 编造实时或来源信息; 只给泛泛而谈的建议`,
      failureModesToProbe: `泛化回答; 约束遗漏; unsupported certainty; 任务漂移`,
      riskLevel: caseType === "boundary_risk" ? "high" : "medium",
      difficulty: ["scripted_multi_turn", "adaptive_multi_turn", "boundary_risk"].includes(caseType) ? "hard" : "medium",
      discriminativeSignal: `${challenger} 是否能在 ${taskLabel} 的 ${capability} 上提供比 ${baseline} 更有任务价值的结果。`,
      turn2UserMessage: caseType.includes("multi_turn") ? `我再补充一个约束：请把${risk}也考虑进去，并说明你为什么这样判断。` : "",
      turn3UserMessage: caseType.includes("multi_turn") ? "不用继续问太多了，请直接给我最终建议，并标注哪些地方需要我自己核验。" : "",
      stopCondition: caseType.includes("multi_turn") ? "输出最终可执行建议后停止。" : "",
      notes: reason ? `Fallback package because local Codex generation failed: ${reason}` : "Fallback package generated by SBS scaffold.",
    };
  });
  const pkg = buildRuntimePackageFromCases(records.map((record, index) => recordToEvalCase(record, activeTask, index + 1)), activeTask);
  pkg.generationTrace.generator = "sbs_fallback_scaffold_after_local_codex_failure";
  pkg.generationTrace.knownLimitations = [
    "This package was generated by a deterministic fallback scaffold because Local Codex did not return a package.",
    "It is structurally valid but weaker than the full chatbot-eval-set-generator skill output.",
  ];
  pkg.selfCritiqueTrace.findings.unshift({
    findingId: "fallback-generator-used",
    severity: "major",
    component: "overall",
    issue: "Local Codex generation failed, so SBS used a deterministic fallback scaffold.",
    whyItMatters: "The package can unblock UI review, but case originality and discriminative coverage may be weaker.",
    recommendedFix: "Retry Local Codex generation later or manually edit/replace cases before collection.",
    fixApplied: false,
    remainingRisk: reason || "Local Codex did not return a package.",
    confirmationBacklogRef: "fallback_generation_review",
    affectedCaseRefs: pkg.evalCases.map((item) => item.caseId),
    affectedDimensionRefs: pkg.evalSetCoveragePlan.enabledDimensions,
  });
  pkg.selfCritiqueTrace.overallReadiness = "needs_user_confirmation";
  pkg.selfCritiqueTrace.unresolvedConfirmationBacklogRefs = [
    ...new Set([...(pkg.selfCritiqueTrace.unresolvedConfirmationBacklogRefs || []), "fallback_generation_review"]),
  ];
  pkg.confirmationBacklog.unshift({
    id: "fallback_generation_review",
    component: "overall",
    relatedObjectIds: pkg.evalCases.map((item) => item.caseId),
    question: "Local Codex generation failed and SBS used a fallback scaffold. Do you want to keep, edit, or retry generation?",
    whyItMatters: "Fallback cases are structurally valid but may be less sharp than the skill-generated eval set.",
    recommendedDefault: "Review/edit cases, or retry Local Codex generation before serious collection.",
    severity: "important",
    blocksEvalRun: false,
    status: "pending_user",
  });
  return pkg;
}

