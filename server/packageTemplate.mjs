function recordToEvalCase(record, activeTask = {}, index) {
  const caseId = record.caseId || `manual-${String(index).padStart(3, "0")}`;
  const modelFacingPrompt = record.modelFacingPrompt || record.initialPrompt || record.scenario;
  const mustDo = splitList(record.mustDo);
  const mustNotDo = splitList(record.mustNotDo);
  const failureModes = splitList(record.failureModesToProbe);
  return {
    caseId,
    caseType: sanitizeCaseType(record.caseType),
    capabilityCluster: record.capabilityCluster || "manual_import",
    scenarioArchetype: record.scenarioArchetype || "manual_import",
    scenario: record.scenario || modelFacingPrompt,
    userPersona: record.userPersona || activeTask.taskSpace?.targetAudience || "Target user",
    userGoal: record.userGoal || record.expectedOutcome || "Complete the task successfully.",
    userFacingIntent: record.userGoal || record.scenario || modelFacingPrompt,
    evaluatorIntent: record.expectedOutcome || "Evaluate whether the answer satisfies the task outcome.",
    hiddenIntent: record.notes || "Manual-imported case; use evaluator fields only for grading context.",
    constraints: [...mustDo, ...mustNotDo.map((item) => `Avoid: ${item}`)],
    initialPrompt: modelFacingPrompt,
    modelFacingPrompt,
    collectionInstructions: ["Send the model-facing prompt manually to both products.", "Capture final output and visible evidence."],
    doNotRevealToModel: ["evaluatorIntent", "hiddenIntent", "expectedOutcome", "failureModesToProbe"],
    exposureContract: {
      modelVisibleFactsAtStart: [modelFacingPrompt],
      evaluatorOnlyFacts: [record.userPersona, record.expectedOutcome, record.notes].filter(Boolean),
      hardScoringRequirements: mustDo.map((requirement) => ({
        requirement,
        source: "model_facing_prompt",
        eligibleFromTurn: 1,
        relatedFields: ["modelFacingPrompt", "mustDo"],
      })),
      inferenceTargets: [record.discriminativeSignal || "task success"].filter(Boolean),
      nonScoringContext: [record.notes].filter(Boolean),
      fairnessNotes: "Hard scoring should use only model-visible facts or reasonable inference.",
    },
    expectedOutcome: record.expectedOutcome || "A useful, accurate, constraint-aware answer.",
    acceptableOutcomes: ["Satisfies the user goal with clear tradeoffs and appropriate uncertainty."],
    unacceptableOutcomes: ["Ignores the user goal, invents unsupported facts, or gives unsafe/confidently wrong claims."],
    mustDo: mustDo.length ? mustDo : ["Address the user goal", "Respect visible constraints"],
    mustNotDo: mustNotDo.length ? mustNotDo : ["Invent unsupported facts"],
    failureModesToProbe: failureModes.length ? failureModes : ["generic answer", "constraint omission", "unsupported certainty"],
    riskLevel: ["low", "medium", "high"].includes(record.riskLevel) ? record.riskLevel : "medium",
    difficulty: ["easy", "medium", "hard"].includes(record.difficulty) ? record.difficulty : "medium",
    discriminativeSignal: record.discriminativeSignal || "Whether the model produces a genuinely useful task outcome.",
    evidenceRequired: { minimumLevel: "L0", notes: "Manual-imported cases can run with final output only." },
    collectionMode: record.caseType?.includes("multi_turn") ? "manual_multi_turn" : "manual_single_prompt",
    estimatedUserEffort: record.caseType?.includes("multi_turn") ? "medium" : "low",
    graderRefs: ["manual_import_default_rubric"],
    confirmationBacklogRefs: [],
    importNotes: record.notes || "",
    manualTemplateTurns: [record.turn2UserMessage, record.turn3UserMessage].filter(Boolean),
    manualStopCondition: record.stopCondition || "",
  };
}

